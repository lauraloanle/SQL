#link: https://leetcode.com/problems/activity-participants/

SELECT ACTIVITY FROM FRIENDS GROUP BY ACTIVITY
HAVING COUNT(ACTIVITY) NOT IN
(
(SELECT COUNT(ACTIVITY) AS NEW FROM FRIENDS GROUP BY ACTIVITY 
 ORDER BY NEW DESC LIMIT 1),
 (SELECT COUNT(ACTIVITY) AS NEW FROM FRIENDS GROUP BY ACTIVITY 
 ORDER BY NEW ASC LIMIT 1)
);
